function doDisplay(){
    var con = document.getElementById("projects-content-div1-folded");
    if(con.style.maxHeight == '70px'){
        con.style.maxHeight = '0px';
        con.style.transition = 'transition: max-height 0.1s ease-out;'
        document.getElementById('projects-content-div1-icon').innerHTML = '<span class="material-icons">arrow_right</span>'
    }else{
        con.style.maxHeight = '70px';
        con.style.transition = 'transition: max-height 0.1s ease-out;'
        document.getElementById('projects-content-div1-icon').innerHTML = '<span class="material-icons">arrow_drop_down</span>'
    }
}


